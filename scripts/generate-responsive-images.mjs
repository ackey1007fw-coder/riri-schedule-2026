import { mkdir, readdir, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const imagesRoot = path.join(root, "public", "images");
const optimizedRoot = path.join(imagesRoot, "optimized");
const manifestPath = path.join(root, "src", "data", "imageManifest.ts");
const targetWidths = [360, 480, 720, 960, 1280, 1600];
const imageExtensions = new Set([".jpg", ".jpeg", ".png"]);

const toPosix = (value) => value.split(path.sep).join("/");

async function walk(dir, { skipOptimized = false, extensions = imageExtensions } = {}) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (skipOptimized && fullPath.startsWith(optimizedRoot)) continue;
      files.push(...await walk(fullPath, { skipOptimized, extensions }));
      continue;
    }
    if (entry.isFile() && extensions.has(path.extname(entry.name).toLowerCase())) {
      files.push(fullPath);
    }
  }

  return files;
}

async function shouldWrite(inputPath, outputPath) {
  try {
    const [inputInfo, outputInfo] = await Promise.all([
      stat(inputPath),
      stat(outputPath)
    ]);
    return outputInfo.mtimeMs < inputInfo.mtimeMs;
  } catch {
    return true;
  }
}

function outputPathFor(relativePath, width) {
  const parsed = path.parse(relativePath);
  return path.join(optimizedRoot, parsed.dir, `${parsed.name}-${width}.webp`);
}

async function optimizeImage(inputPath) {
  const relativePath = path.relative(imagesRoot, inputPath);
  const publicPath = `/images/${toPosix(relativePath)}`;
  const metadata = await sharp(inputPath).metadata();
  const sourceWidth = metadata.width ?? 0;
  const sourceHeight = metadata.height ?? 0;

  if (!sourceWidth || !sourceHeight) return null;

  const widths = targetWidths.filter((width) => width < sourceWidth);
  widths.push(sourceWidth);
  const uniqueWidths = [...new Set(widths)].sort((a, b) => a - b);

  for (const width of uniqueWidths) {
    const outputPath = outputPathFor(relativePath, width);
    if (!await shouldWrite(inputPath, outputPath)) continue;
    await mkdir(path.dirname(outputPath), { recursive: true });
    await sharp(inputPath)
      .rotate()
      .resize({ width, withoutEnlargement: true })
      .webp({ quality: 78, effort: 4 })
      .toFile(outputPath);
  }

  return {
    publicPath,
    width: sourceWidth,
    height: sourceHeight,
    widths: uniqueWidths
  };
}

function manifestSource(entries) {
  const body = entries
    .sort((a, b) => a.publicPath.localeCompare(b.publicPath))
    .map((entry) => {
      const value = JSON.stringify({
        width: entry.width,
        height: entry.height,
        widths: entry.widths
      });
      return `  ${JSON.stringify(entry.publicPath)}: ${value}`;
    })
    .join(",\n");

  return `// Generated by scripts/generate-responsive-images.mjs. Do not edit by hand.\nexport const imageManifest = {\n${body}\n} as const;\n\nexport type ImageManifestPath = keyof typeof imageManifest;\n`;
}

const files = await walk(imagesRoot, { skipOptimized: true });
const entries = [];

for (const file of files) {
  const entry = await optimizeImage(file);
  if (entry) entries.push(entry);
}

await writeFile(manifestPath, manifestSource(entries), "utf8");

const originalBytes = await Promise.all(files.map((file) => stat(file).then((info) => info.size)));
const optimizedFiles = await walk(optimizedRoot, { extensions: new Set([".webp"]) }).catch(() => []);
const optimizedBytes = await Promise.all(optimizedFiles.map((file) => stat(file).then((info) => info.size)));
const sum = (values) => values.reduce((total, value) => total + value, 0);

console.log(`Optimized ${entries.length} images.`);
console.log(`Original bytes: ${sum(originalBytes)}`);
console.log(`Generated WebP bytes: ${sum(optimizedBytes)}`);
